export const DICTS: Record<Lang, Dict> = { ar, en, hi, fa, fr, ur };

// ---------------- i18n context ----------------
type I18nValue = {
  t: (k: string, vars?: Record<string, string | number>) => string;
  lang: Lang;
  dir: "rtl" | "ltr";
  setLang: (lang: Lang) => void;
  supported: Lang[];
};

const RTL_LANGS: Lang[] = ["ar", "fa", "ur"];
const SUPPORTED: Lang[] = ["ar", "en", "hi", "fa", "fr", "ur"];

function guessInitialLang(): Lang {
  if (typeof window === "undefined") return "ar";
  const saved = localStorage.getItem("lang") as Lang | null;
  if (saved && SUPPORTED.includes(saved)) return saved;
  const nav = navigator.language?.toLowerCase() || "";
  if (nav.startsWith("ar")) return "ar";
  if (nav.startsWith("fa")) return "fa";
  if (nav.startsWith("ur")) return "ur";
  if (nav.startsWith("hi")) return "hi";
  if (nav.startsWith("fr")) return "fr";
  return "en";
}

const I18nContext = createContext<I18nValue>({
  t: (k) => k,
  lang: "ar",
  dir: "rtl",
  setLang: () => {},
  supported: SUPPORTED,
});

export function I18nProvider({ children, initialLang }: { children: React.ReactNode; initialLang?: Lang }) {
  const [lang, setLangState] = useState<Lang>(initialLang ?? guessInitialLang());
  const dir: "rtl" | "ltr" = RTL_LANGS.includes(lang) ? "rtl" : "ltr";

  const setLang = (l: Lang) => {
    setLangState(l);
    if (typeof window !== "undefined") {
      localStorage.setItem("lang", l);
    }
  };

  useEffect(() => {
    if (typeof document !== "undefined") {
      document.documentElement.setAttribute("dir", dir);
      document.documentElement.setAttribute("lang", lang);
    }
  }, [lang, dir]);

  const t = useMemo(() => {
    const dict = DICTS[lang] || en;
    return (k: string, vars?: Record<string, string | number>) => {
      // prefer current lang, then English, then Arabic, else key
      let s = (dict[k] ?? en[k] ?? ar[k] ?? k);
      if (vars) {
        for (const [vk, vv] of Object.entries(vars)) {
          // Avoid RegExp to support {0}, {year}, etc.
          s = s.split(`{${vk}}`).join(String(vv));
        }
      }
      return s;
    };
  }, [lang]);

  const value: I18nValue = { t, lang, dir, setLang, supported: SUPPORTED };
  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() { return useContext(I18nContext); }

// Helpers for legacy/utility usage
export function getT(router?: Partial<NextRouter> | { locale?: string } | string) {
  let lang: Lang = "ar";
  if (typeof router === "string") {
    lang = (router as Lang) && SUPPORTED.includes(router as Lang) ? (router as Lang) : (router === "en" ? "en" : "ar");
  } else if (router && typeof router === "object" && "locale" in router && (router as any).locale) {
    const lc = String((router as any).locale).toLowerCase();
    const candidate = (lc.split("-")[0] as Lang);
    lang = SUPPORTED.includes(candidate) ? candidate : (lc.startsWith("en") ? "en" : "ar");
  } else if (typeof window !== "undefined") {
    const saved = localStorage.getItem("lang") as Lang | null;
    lang = saved && SUPPORTED.includes(saved) ? saved : guessInitialLang();
  }
  const dict = DICTS[lang] || en;
  return (k: string, vars?: Record<string, string | number>) => {
    let s = (dict[k] ?? en[k] ?? ar[k] ?? k);
    if (vars) {
      for (const [vk, vv] of Object.entries(vars)) {
        s = s.split(`{${vk}}`).join(String(vv));
      }
    }
    return s;
  };
}

export function getDir(locale?: string) {
  const lc = (locale || "").toLowerCase();
  const lang = (lc.split("-")[0] as Lang) || "ar";
  return RTL_LANGS.includes(lang) ? "rtl" : "ltr";
}

export const SUPPORTED_LANGS = SUPPORTED;
export const IS_RTL = (l: Lang) => RTL_LANGS.includes(l);
